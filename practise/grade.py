score = float(input('Enter your exam score: '))
if score >= 80 :
	print('You have grade A !!')
elif score >= 75:
	print('You have grade B+ ')
elif score >= 70:
	print('You have grade B ')
elif score >= 65:
	print('You have grade C+ ')
elif score >= 60:
	print('You have grade C ')
elif score >= 55:
	print('You have grade D+ ')
elif score >= 50:
	print('You have grade D ')
else :
	print('You have grade F')

